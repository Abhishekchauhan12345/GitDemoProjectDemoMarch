package importantProgram3;

public class SwappingOfTwoNumber {

	public static void main(String[] args) {
		
		int a=10, b=20, c=0;
		
		System.out.println("before swapping:"+ a+ " "+ b);
		
		c=a;
		a=b;
		b=c;
		System.out.println("after swapping:"+ a+" "+ b);
		
		
		
		

	}

}
